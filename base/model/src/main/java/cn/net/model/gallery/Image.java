package cn.net.model.gallery;

public class Image {
    private String url;
    private String buckId;
    private String buckName;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getBuckId() {
        return buckId;
    }

    public void setBuckId(String buckId) {
        this.buckId = buckId;
    }

    public String getBuckName() {
        return buckName;
    }

    public void setBuckName(String buckName) {
        this.buckName = buckName;
    }
}
